Voici quelques petites astuces, infos, remaques au sujet de l'utilisation d'iTP et de ses différents modules:
<br><br>

<h3>iTP</h3>
<ul>
	<li>Un reset de password peut être réalisé par un administrateur, le nouveau mot de passe devient : « changeme ».</li>
	<li>Il est préférable de classer les actions d'une étape de la manière suivante : INFO et ensuite ordre par « classement ».	</li>
	<li></li>
</ul>
<br><br>

<h3>Workflow management</h3>
<ul>
	<li>Un patient est distinct d'un autre de part: son IDStluc, son numéro de Course, un volume cible, un PTV, un Plan.</li>
	<li>Il faut bien sûr remplir toutes les actions d'une étape pour pouvoir passer à la suivante.</li>
	<li>Il n'y a pas de raison de mettre à jour une étape du futur, donc pas d'enregistrement des cases éventuellement remplies dans une étape ultérieure. Tout comme pour le passé. Ce qui est fait est fait.
	Mais on pourra ajuster les choix par la suite (ex: changement de machine).</li>
	<li>La création d'un nouveau workflow/process a un impact direct sur les patients qui seront introduits après modification de celui-ci.</li>
	<li>La création d'un nouveau PTV ou d'un nouveau plan pour une autre machine de traitement prendra le workflow en cours, donc éventuellement différent de celui utilisé par le patient PTV1 ou Plan1.</li>
	<li>Une étape/action n'est jamais effacée mais peut être désactivée (car des patients plus anciens doivent conserver leur process).</li>
	<li>Le classement des actions peut être organisé dans la partie administrative de gestion du processus, mais ne sera effectif que pour les patients suivants.</li>
	<li>Une * après le nom de l'action indique que celle-ci est obligatoire.</li>
</ul>
